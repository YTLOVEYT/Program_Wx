    /** 生成emotion的entity，带图标+文本+类型 */
    private static Emotion[] createData()
    {
        Emotion[] datas = new Emotion[icons.length];
        for (int i = 0; i < icons.length; ++i)
        {
            datas[i] = new Emotion(icons[i], emojis[i], Emotion.EmotionType.NORMAL);
        }
        return datas;
    }

    private static final Emotion[] DATA = createData();//获取全部封装好的表情

